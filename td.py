# materi Tipe Data 
# Tipe data String (str)
nama = "Sania nabila putri"
print(nama)  # Menampilkan nama

# Tipe data Integer (int)
usia = 15
print(usia)  # Menampilkan usia

# Tipe data Float (float)
kelas = 10
print(kelas)  # Menampilkan kelas

# Tipe data Boolean (bool)
is_siswa = True
print(is_siswa)  # Menampilkan True

# Tipe data List (list)
hobi = ["membaca", "menulis"]
print(hobi)  # Menampilkan list hobi


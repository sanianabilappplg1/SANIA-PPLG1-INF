# Materi Variabel 

# Variabel adalah tempat untuk menyimpan data

# Contoh variabel
nama = "sania"         # Menyimpan data string
usia = 15              # Menyimpan data integer
kelas = 10             # Menyimpan data float
is_siswa = True        # Menyimpan data boolean

# Menampilkan nilai dari variabel
print("Nama:", nama)
print("Usia:", usia)
print("kelas:", kelas)
print("Apakah sania seorang siswa?:", is_siswa)

# WHILE LOOP
i = 1
while i <= 2:
    print(f"Nama saya sania, ini perulangan ke-{i}")
    i += 1

# FOR LOOP
for i in range(1, 2):
    print(f"Nama saya sania, ini perulangan ke-{i}")

# NESTED LOOP
for i in range(1, 2):
    for j in range(1, 2):
        print(f"sania - Baris {i}, Kolom {j}")